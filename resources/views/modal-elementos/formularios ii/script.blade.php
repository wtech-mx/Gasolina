<script type="text/javascript">
    function mostrar2(id) {
        if (id == "ii-SASISOPA") {
            $("#ii-SASISOPA").show();
            $("#ii-SGM").hide();
        }

        if (id == "ii-SGM") {
            $("#ii-SASISOPA").hide();
            $("#ii-SGM").show();
        }
    }
</script>
