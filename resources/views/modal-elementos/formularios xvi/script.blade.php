<script type="text/javascript">
    function mostrar16(id) {
        if (id == "xvi") {
            $("#xvi").show();
            $("#xvi-01").hide();
        }
        if (id == "xvi-01") {
            $("#xvi").hide();
            $("#xvi-01").show();
        }
    }

    function mostrars16(id) {
        if (id == "xvi-01-01") {
            $("#xvi-01-01").show();
            $("#xvi-01-02").hide();
        }
        if (id == "xvi-01-02") {
            $("#xvi-01-01").hide();
            $("#xvi-01-02").show();
        }
    }
</script>
