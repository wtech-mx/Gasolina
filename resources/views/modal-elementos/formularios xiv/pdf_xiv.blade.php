               <strong> 3.2.1.2.-Tanques:</strong><br><br>
                La exactitud de las mediciones en los tanques verticales depende de la exactitud en las mediciones de
                nivel y sustancialmente de la calibración del tanque. La conversión de nivel del tanque a volumen
                contenido se realiza mediante el uso de las tablas de calibración proporcionadas por la empresa
                responsable de la calibración del tanque.<br><br>
                a). La calibración de los tanques verticales debe ser realizada por un Laboratorio de calibración, en los
                términos de la LFMN y su Reglamento. La calibración puede ser realizada mediante métodos de
                carácter dimensional o bien usando un medidor de caudal.<br>
                b). La incertidumbre de calibración de los tanques verticales debe ser menor o igual que 0.2 %.<br>
                c). Los tanques deben ser verificados una vez cada 5 años con la finalidad de determinar la ocurrenciabr
                de: distorsiones en el fondo del tanque, protuberancias en el tanque, inclinación del tanque,
                distorsiones en la lámina del tanque, e) modificación de la altura de referencia, modificación de los
                circuitos hidráulicos y estructurales del interior del tanque.<br>
                Cualquiera de los aspectos enunciados puede ocasionar cambios en el volumen contenido en el
                tanque vertical, por lo que en caso de ocurrir cualquiera de las anomalías citadas, deberán de
                realizarse las correcciones correspondientes en las tablas de calibración.
                Dentro del sistema GASAFE se subirá el soporte documental (certificado de calibración de cada
                tanque) para garantizar y resguardar la evidencoia de las calibraciones realizadas a los tanques de
                almacenamiento.<br><br>
                <strong>3.2.1.3.-Calibración de Jarra Patron</strong><br><br>
                La cañlibración de la Jarra patrón se deberá de realizar por lo menos una vez al año, para garantizar
                que los monitoreos de los dispensarios están siendo eficientes y no tener una sorpresa en el momento
                de una inspección por alguna autoridad competente.
                La calibración se deberá de llevar acabo por un laboratorio acreditado para la calibración de equipos
                de medición, la cual deberá de entregar un certificado de la calibración, este documento se deberá de
                subir en el sistema GASAFE.<br><br>
               <strong> 3.2.1.4.-Calibración de Regleta o cinta para la determinación de volumen.</strong>
                Las cintas de medición deben cumplir con una especificación de error máximo permisible (EMP) de ±
                1.5 mm cuando son nuevas; mientras que una vez que son puestas en servicio, el error máximo
                permisible es ± 2.0 mm. <br>
                Una vez puestas en operación estas se deberán de calibrar por lo menos una vez al año, en caso de
                que la estación de servicio cuente con este equipo, si se contrata el servicio se deberá de asegurar que
                la cinta o regleta de calibración se encuentra calibrada. <br>
                Los equipos anteriormente mencionados quedaran establecidos en el plan y programa de calibración
